package arrays;

public class String {

	public static void main(java.lang.String[] args) {
		
		java.lang.String a= "nisha";
		System.out.println(a.concat("nthi"));//Strings are immutable.
		System.out.println(a);
		
		StringBuilder s1 = new StringBuilder("sdsdsd");
		s1.append("jhjjh");
		System.out.println(s1);
		
		StringBuffer sb = new StringBuffer("subhi");
		sb.append("ksha");
		System.out.println(sb);


	}
	
	

}
