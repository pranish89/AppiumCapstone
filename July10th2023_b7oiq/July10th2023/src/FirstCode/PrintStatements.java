package FirstCode;

public class PrintStatements {

	public static void main(String[] args) {

		// joins the String - Concatenation
		System.out.println("100" + "\n" + "100");

		// + sign will act as arithmetic operator
		System.out.println(10 + 30 + 40 + 30); // 110

		// Concatenation
		System.out.println("Spartans " + 300);

		System.out.println("My room number is " + 25);
		
		//BODMAS
		System.out.println("Your ranking is " + (20 * 30 + 40));
	
	}

}
